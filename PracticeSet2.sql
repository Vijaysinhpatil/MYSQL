SELECT * FROM STUDENT;

-- 1. SELECT ALL THE STUDENTS WHO SCORED 75+.
SELECT NAME , MARKS FROM STUDENT WHERE MARKS >= 75;

-- 2. FIND ALL NAMES OF CITIES WHERE THE STUDENTS ARE.
SELECT CITY FROM STUDENT GROUP BY CITY;

-- 3. FIMD THE MAXIMUM MARKS FROM EACH CITY
SELECT MAX(MARKS) , CITY FROM STUDENT GROUP BY CITY;

-- 4. FIND THE AVERAGE MARKS OF THE CLASS
SELECT AVG(MARKS) FROM STUDENT;

