-- Create a table to store student information with the following fields:
-- roll_no, name, city, marks

-- Insert the following records into the table:
-- (110, 'adam', 'Delhi', 76)
-- (108, 'bob', 'Mumbai', 65)
-- (124, 'casey', 'Pune', 94)
-- (112, 'duke', 'Pune', 80)

CREATE TABLE STUDENT (
ROLL_NO INT PRIMARY KEY,
NAME VARCHAR(30),
CITY VARCHAR(30),
MARKS INT
);

